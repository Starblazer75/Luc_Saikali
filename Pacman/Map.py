def make_block(x, y):
    block = turtle.Turtle()
    block.penup()
    block.shape("square")
    block.shapesize(1.5, 1.5)
    block.color("white")
    real_coordinates = map_coordinates(x, y)
    block.goto(real_coordinates[0], real_coordinates[1])


def map_coordinates(x, y):
    coordinates = [x, y]
    coordinates[0] = (-8 * 30) + (30 * x)
    coordinates[1] = (-10 * 30) + (30 * y) + 16
    return coordinates
